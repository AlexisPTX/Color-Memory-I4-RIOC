package pinoteaux.projetrioc;

/**
 * La classe Constantes contient les valeurs constantes utilisées dans le projet pour la configuration des serveurs.
 * Ces constantes incluent les adresses de serveur et les ports associés à chaque serveur, utilisés pour établir des connexions réseau.
 */
public class Constantes {

    /**
     * L'adresse du serveur, utilisée pour les connexions. Ici, l'adresse est configurée sur "localhost" (127.0.0.1).
     */
    public static final String SERVER_ADDRESS = "localhost";

    /**
     * Le port global du serveur utilisé pour les connexions générales et la discussion globale.
     */
    public static final int SERVER_GLOBAL_PORT = 9999;

    /**
     * Le port utilisé pour le serveur 1.
     */
    public static final int SERVER_1_PORT = 1111;

    /**
     * Le port utilisé pour le serveur 2.
     */
    public static final int SERVER_2_PORT = 2222;

    /**
     * Le port utilisé pour le serveur 3.
     */
    public static final int SERVER_3_PORT = 3333;

    /**
     * Le port utilisé pour le serveur 4.
     */
    public static final int SERVER_4_PORT = 4444;

    /**
     * Le port utilisé pour le serveur 5.
     */
    public static final int SERVER_5_PORT = 5555;
}
